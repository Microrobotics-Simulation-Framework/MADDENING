def _ensure_stable_only_or_opt_in(
    surface_name: str,
    include_evolving: bool,
) -> bool:
    """Return True iff the named surface is allowed into the FMU.

    The rule (per the v0.3.0 plan §A1): a variable enters the FMU
    surface iff its source/sink is tagged ``@stability(STABLE)`` and
    the caller explicitly opts it in via the FMU export config — *not*
    by default, to keep the public FMU surface minimal.

    The caller's opt-in is selecting which nodes / fields to export.
    The audit-level check is "are the source/sink classes stable?" —
    that's what this function answers.
    """
    level = _STABILITY_REGISTRY.get(surface_name)
    if level == StabilityLevel.STABLE:
        return True
    if include_evolving and level in (
        StabilityLevel.EVOLVING, StabilityLevel.PROVISIONAL,
    ):
        return True
    return False


def _deterministic_token(parts: Iterable[str]) -> str:
    """Build a stable, GUID-shaped token from a list of strings.

    We don't use Python's ``uuid`` because the token must be a pure
    function of the graph's structural fingerprint (any FMU loader
    refuses to instantiate when the token doesn't match the runtime
    DLL's compile-time token).  See FMI 3.0 §2.2.3.
    """
    import hashlib  # noqa: PLC0415 — stdlib lazy import
    h = hashlib.sha256()
    for part in parts:
        h.update(part.encode("utf-8"))
        h.update(b"\0")
    digest = h.hexdigest()
    # Format as 8-4-4-4-12 GUID for tooling that pattern-matches it.
    return f"{digest[:8]}-{digest[8:12]}-{digest[12:16]}-{digest[16:20]}-{digest[20:32]}"


@stability(StabilityLevel.EVOLVING)
def build_model_description(
    graph_manager: Any,
    *,
    model_name: str,
    selected_inputs: Optional[Iterable[str]] = None,
    selected_outputs: Optional[Iterable[tuple[str, str]]] = None,
    include_evolving: bool = False,
    default_step_size: Optional[float] = None,
) -> ModelDescription:
    """Build an FMI 3.0 :class:`ModelDescription` from a ``GraphManager``.

    Parameters
    ----------
    graph_manager : GraphManager
        A *compiled* graph.  The structure (nodes, edges, external
        inputs) is read at build time.
    model_name : str
        Identifier the FMU will be called by.  Becomes ``modelName``
        in the XML.
    selected_inputs : iterable of str, optional
        FMU input variables to surface.  Names must match the graph's
        registered external-input keys.  When ``None`` (default),
        every external input is included.
    selected_outputs : iterable of (node, field) tuples, optional
        Which per-node state fields to expose as FMU outputs.  When
        ``None`` (default), every state field of every node is
        included.
    include_evolving : bool, default False
        If ``True``, also include surfaces tagged
        ``@stability(EVOLVING)`` or ``PROVISIONAL``.  Default keeps
        the FMU's public surface minimal — only ``STABLE``-tagged
        sources/sinks contribute.
    default_step_size : float, optional
        Default fixed step size for the FMU's experiment block.
        Defaults to the graph's master timestep when available.

    Returns
    -------
    ModelDescription
    """
    # Inputs: read external-input edges from the graph manager.  The
    # graph manager exposes them via ``_external_input_specs`` (an
    # internal dict) — we accept a duck-typed view for tests.
    ext_specs = getattr(graph_manager, "_external_input_specs", {})
    selected_input_keys = (
        set(selected_inputs) if selected_inputs is not None
        else set(ext_specs.keys())
    )

    variables: list[FMIVariable] = []
    next_vr = 1  # FMI uses 1-based valueReference

    # ----- Inputs -----
    for ext_key, ext_spec in ext_specs.items():
        if ext_key not in selected_input_keys:
            continue
        dtype = getattr(ext_spec, "dtype", "float32")
        if not isinstance(dtype, str):
            dtype = getattr(dtype, "name", str(dtype))
        shape = tuple(getattr(ext_spec, "shape", ()) or ())
        unit = getattr(ext_spec, "expected_units", "") or ""
        variables.append(FMIVariable(
            name=ext_key,
            value_reference=next_vr,
            dtype=dtype,
            causality="input",
            variability="continuous",
            description=getattr(ext_spec, "description", "") or "",
            unit=unit,
            shape=shape or None,
        ))
        next_vr += 1

    # ----- Outputs -----
    nodes = getattr(graph_manager, "_nodes", {})
    selected_output_set = (
        set((n, f) for (n, f) in (selected_outputs or []))
    )
    for node_name, node_spec in nodes.items():
        node = getattr(node_spec, "node", node_spec)
        node_class_name = (
            f"{type(node).__module__}.{type(node).__name__}"
        )
        if not _ensure_stable_only_or_opt_in(node_class_name, include_evolving):
            # Node's class isn't part of the public FMU surface yet.
            continue
        for field_name in node.state_fields():
            if selected_outputs is not None and \
                    (node_name, field_name) not in selected_output_set:
                continue
            initial = node.initial_state()[field_name]
            dtype = str(getattr(initial, "dtype", "float32"))
            shape = tuple(getattr(initial, "shape", ()) or ())
            variables.append(FMIVariable(
                name=f"{node_name}.{field_name}",
                value_reference=next_vr,
                dtype=dtype,
                causality="output",
                variability="continuous",
                description=f"State field {field_name!r} of node "
                            f"{node_name!r}",
                shape=shape or None,
            ))
            next_vr += 1

    # ----- Defaults -----
    if default_step_size is None:
        default_step_size = float(
            getattr(graph_manager, "_master_timestep", 1.0e-3) or 1.0e-3
        )

    # Deterministic instantiationToken — depends on the schema, so
    # the FMU loader refuses mismatched schemas at instantiation.
    token = _deterministic_token([
        model_name,
        *[f"{v.name}:{v.dtype}:{v.causality}:{v.shape!r}" for v in variables],
        f"step:{default_step_size}",
    ])

    return ModelDescription(
        model_name=model_name,
        instantiation_token=token,
        variables=variables,
        default_step_size=default_step_size,
    )


