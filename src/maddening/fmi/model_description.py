"""``modelDescription.xml`` generator for FMI 3.0 (v0.3.0 §A1).

Reads a :class:`GraphManager`'s public surface together with the
``@stability`` audit registry to emit a minimal FMI 3.0
``modelDescription.xml``.  Conservative by default: only surfaces
tagged ``@stability(STABLE)`` enter the FMU unless the caller opts in
via ``include_evolving=True`` (mirrors STACK_V1 M4's stability gate).

What's included for v0.3.0:

* ``<fmiModelDescription>`` root with required attributes (FMI 3.0,
  ``modelName``, ``instantiationToken``, units).
* ``<ModelVariables>`` populated from the graph's external-input
  edges and per-node state fields.
* ``<ModelStructure>`` ``<Output>`` and ``<InitialUnknown>`` entries
  consistent with the variables.
* Default experiment block (start / stop / tolerance / step size
  derived from the graph's master timestep).

What's deferred:

* Full ``<UnitDefinitions>`` (the v0.3.0 generator emits the unit
  *strings* attached to inputs but doesn't enumerate them in a
  central registry — FMPy round-trips this fine, but a strict
  importer might prefer the registry form).
* ``<TypeDefinitions>`` — added if/when a consumer asks.
* Multi-clock ``<ClockType>`` definitions (designed-in but not
  emitted by default — v0.3.0 ships single-clock per A1).
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Iterable, Optional
import xml.etree.ElementTree as ET

from maddening.core.compliance.metadata import StabilityLevel
from maddening.core.compliance.stability import (
    _STABILITY_REGISTRY,
    stability,
)


# ---------------------------------------------------------------------------
# Public dataclasses
# ---------------------------------------------------------------------------


_DTYPE_TO_FMI_TYPE = {
    "float32": "Float32",
    "float64": "Float64",
    "int8": "Int8",
    "int16": "Int16",
    "int32": "Int32",
    "int64": "Int64",
    "uint8": "UInt8",
    "uint16": "UInt16",
    "uint32": "UInt32",
    "uint64": "UInt64",
    "bool": "Boolean",
    "bool_": "Boolean",
}

_CAUSALITIES = {"input", "output", "parameter", "local", "calculatedParameter"}
_VARIABILITIES = {"constant", "fixed", "tunable", "discrete", "continuous"}


@dataclass(frozen=True)
class FMIVariable:
    """A single entry in the FMU's ``<ModelVariables>`` section.

    Attributes
    ----------
    name : str
        FMU variable name.  Convention: ``<node>.<field>`` for
        per-node state fields, ``<edge_name>`` for external inputs.
    value_reference : int
        Unique handle the FMU runtime uses to address this variable.
        Generated by :func:`build_model_description`.
    dtype : str
        Python dtype name (``"float32"``, ``"int32"``, ...).
    causality : {"input", "output", "parameter", "local",
                 "calculatedParameter"}
    variability : {"constant", "fixed", "tunable", "discrete",
                   "continuous"}
    description : str, optional
        Human-readable description.
    unit : str, optional
        Unit string (e.g. ``"m"``, ``"N"``).  Surfaces through
        :class:`EdgeSpec.expected_units` when available.
    shape : tuple of int, optional
        Array shape, for FMI 3.0 dynamic arrays.  Empty / None means
        scalar.
    """
    name: str
    value_reference: int
    dtype: str
    causality: str
    variability: str
    description: str = ""
    unit: str = ""
    shape: Optional[tuple[int, ...]] = None

    def __post_init__(self) -> None:
        if self.causality not in _CAUSALITIES:
            raise ValueError(
                f"FMIVariable.causality={self.causality!r} not in "
                f"{sorted(_CAUSALITIES)}",
            )
        if self.variability not in _VARIABILITIES:
            raise ValueError(
                f"FMIVariable.variability={self.variability!r} not in "
                f"{sorted(_VARIABILITIES)}",
            )
        if self.dtype not in _DTYPE_TO_FMI_TYPE:
            raise ValueError(
                f"FMIVariable.dtype={self.dtype!r} not in "
                f"{sorted(_DTYPE_TO_FMI_TYPE)}",
            )


@dataclass
class ModelDescription:
    """The structured contents of a ``modelDescription.xml`` document.

    Construct via :func:`build_model_description`; serialize to XML
    via :meth:`to_xml`.

    Attributes
    ----------
    model_name : str
        Short identifier for the FMU.  Surfaces in the file name and
        as the ``modelName`` attribute on the root element.
    instantiation_token : str
        A GUID-like string the FMU runtime uses to verify schema
        compatibility on instantiation.  Deterministic — see
        :func:`build_model_description` for the derivation rule.
    fmi_version : str
        FMI standard version.  Defaults to ``"3.0"``.
    generation_tool : str
        Tool string written to ``generationTool`` attribute.
    variables : list[FMIVariable]
        Every variable in the FMU's ``<ModelVariables>`` section.
    default_start_time : float
        Default experiment start time.  Derived from the graph
        configuration if available.
    default_stop_time : float
        Default experiment stop time.
    default_step_size : float
        Default fixed timestep.  Derived from the graph's master
        timestep.
    default_tolerance : float
        Default tolerance for adaptive solvers.
    """
    model_name: str
    instantiation_token: str
    fmi_version: str = "3.0"
    generation_tool: str = "maddening.fmi"
    variables: list[FMIVariable] = field(default_factory=list)
    default_start_time: float = 0.0
    default_stop_time: float = 1.0
    default_step_size: float = 1.0e-3
    default_tolerance: float = 1.0e-6

    def to_xml(self) -> str:
        """Serialize this description to an FMI 3.0 ``modelDescription.xml``
        string (UTF-8, with XML declaration).
        """
        root = ET.Element("fmiModelDescription", attrib={
            "fmiVersion": self.fmi_version,
            "modelName": self.model_name,
            "instantiationToken": self.instantiation_token,
            "generationTool": self.generation_tool,
        })

        # <DefaultExperiment>
        ET.SubElement(root, "DefaultExperiment", attrib={
            "startTime": repr(self.default_start_time),
            "stopTime": repr(self.default_stop_time),
            "tolerance": repr(self.default_tolerance),
            "stepSize": repr(self.default_step_size),
        })

        # <ModelVariables>
        mv = ET.SubElement(root, "ModelVariables")
        for var in self.variables:
            fmi_type = _DTYPE_TO_FMI_TYPE[var.dtype]
            attrib = {
                "name": var.name,
                "valueReference": str(var.value_reference),
                "causality": var.causality,
                "variability": var.variability,
            }
            if var.description:
                attrib["description"] = var.description
            v_el = ET.SubElement(mv, fmi_type, attrib=attrib)
            if var.unit:
                v_el.set("unit", var.unit)
            if var.shape:
                # FMI 3.0 dynamic arrays — emit one <Dimension> per axis.
                for dim in var.shape:
                    ET.SubElement(v_el, "Dimension", attrib={"start": str(dim)})

        # <ModelStructure>
        ms = ET.SubElement(root, "ModelStructure")
        for var in self.variables:
            if var.causality == "output":
                ET.SubElement(ms, "Output", attrib={
                    "valueReference": str(var.value_reference),
                })
            if var.variability in ("continuous", "discrete") and \
                    var.causality in ("output", "local"):
                ET.SubElement(ms, "InitialUnknown", attrib={
                    "valueReference": str(var.value_reference),
                })

        # Pretty-printable serialization.
        ET.indent(root)
        return ET.tostring(root, encoding="unicode", xml_declaration=True)


